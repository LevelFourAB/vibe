package se.l4.vibe.probes;

import java.util.Collection;
import java.util.concurrent.TimeUnit;

/**
 * Probes for creating average values.
 * 
 * @author Andreas Holstenson
 *
 */
public class Average
{
	private Average()
	{
	}
	
	/**
	 * Create an average for the entire time series.
	 * 
	 * @param series
	 * @return
	 */
	public static <T extends Number> Probe<Double> forSeries(TimeSeries<T> series)
	{
		return TimeSeriesProbes.forSeries(series, Average.<T>newOperation());
	}
	
	/**
	 * Create an average for the entire time series.
	 * 
	 * @param series
	 * @param reader
	 * @return
	 */
	public static <I, T extends Number> Probe<Double> forSeries(TimeSeries<I> series, ValueReader<I, T> reader)
	{
		return TimeSeriesProbes.forSeries(series, Average.<I, T>newOperation(reader));
	}
	
	/**
	 * Create an average for a time series that will keep an average for the
	 * specified duration.
	 * 
	 * @param series
	 * @param duration
	 * @param unit
	 * @return
	 */
	public static <T extends Number> Probe<Double> forSeries(
			TimeSeries<T> series,
			long duration,
			TimeUnit unit)
	{
		return TimeSeriesProbes.forSeries(series, duration, unit, Average.<T>newOperation());
	}
	
	/**
	 * Create an average for a time series that will keep an average for the
	 * specified duration.
	 * 
	 * @param series
	 * @param duration
	 * @param unit
	 * @return
	 */
	public static <I, T extends Number> Probe<Double> forSeries(
			TimeSeries<I> series,
			ValueReader<I, T> reader,
			long duration,
			TimeUnit unit)
	{
		return TimeSeriesProbes.forSeries(series, duration, unit, Average.<I, T>newOperation(reader));
	}
	
	/**
	 * Create a probe that will average another sampled probe. This type of
	 * probe should be used as part of a {@link TimeSeries time series}.
	 * 
	 * @param probe
	 * @return
	 */
	public static <T extends Number> SampledProbe<Double> forProbe(
			SampledProbe<T> probe)
	{
		return new AveragingProbeProbe<T>(probe);
	}
	
	/**
	 * Create a probe that averages numeric values.
	 * 
	 * @return
	 */
	public static AveragingProbe create()
	{
		return new AveragingProbe();
	}
	
	/**
	 * Create an operation that will calculate an average.
	 * 
	 * @return
	 */
	public static <T extends Number> TimeSeriesOperation<T, Double> newOperation()
	{
		return new AverageOperation<T, T>(ValueReaders.<T>same());
	}
	
	/**
	 * Create an operation that will calculate an average.
	 * 
	 * @return
	 */
	public static <I, T extends Number> TimeSeriesOperation<I, Double> newOperation(ValueReader<I, T> reader)
	{
		return new AverageOperation<I, T>(reader);
	}
	
	/**
	 * Operation that will calculate the average.
	 * 
	 * @author Andreas Holstenson
	 *
	 * @param <T>
	 */
	private static class AverageOperation<I, O extends Number>
		implements TimeSeriesOperation<I, Double>
	{
		private final ValueReader<I, O> reader;
		
		private double totalSum;
		private double totalEntries;
		
		public AverageOperation(ValueReader<I, O> reader)
		{
			this.reader = reader;
		}
		
		@Override
		public void add(I value, Collection<TimeSeries.Entry<I>> entries)
		{
			totalSum += reader.read(value).doubleValue();
			totalEntries += 1;
		}

		@Override
		public void remove(I value, Collection<TimeSeries.Entry<I>> entries)
		{
			totalSum -= reader.read(value).doubleValue();
			totalEntries -= 1;
		}
		
		@Override
		public Double get()
		{
			return totalSum / totalEntries;
		}
	}
	
	/**
	 * Probe that calculates the average of another probe.
	 * 
	 * @author Andreas Holstenson
	 *
	 * @param <T>
	 */
	private static class AveragingProbeProbe<T extends Number>
		extends AbstractSampledProbe<Double>
	{
		private long accumulated;
		private long samples;
		private final SampledProbe<T> probe;

		public AveragingProbeProbe(SampledProbe<T> probe)
		{
			this.probe = probe;
		}

		@Override
		public Double peek()
		{
			long diff = probe.peek().longValue();
			return (accumulated + diff) / (samples + 1.0);
		}

		@Override
		protected Double sample0()
		{
			accumulated += probe.read().longValue(); 
			samples++;
			return accumulated / (double) samples;
		}

	}
}
